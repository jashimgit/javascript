// arr.slice()

// syntax: arr.slice(start, end)

let arr = ['t', 'e', 's', 't'];
console.log(arr.slice(1, 4));
arr.push('a', 'n', 'e', 'w', 'w', 'o', 'r', 'd');
console.log(arr);

let v = arr.slice(2, 6 + 2)
console.log(v);

/**
 *
 *   slice () takes the start index and
 *   ends before end params
 */